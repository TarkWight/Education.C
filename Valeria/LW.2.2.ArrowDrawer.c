//===- ArrowDrawer/main.c - Отображение стрелки в консоли -*- C -*-===//
//
// Данная программа рисует "стрелку" в консоли, используя чередующиеся символы
// '#' и '*' для каждой строки. Программа может работать в двух режимах:
// 1. Пользователь вводит только высоту стрелки.
// 2. Пользователь вводит высоту, ширину краёв и ширину середины стрелки.
//
//===----------------------------------------------------------------------===//
///
/// \file
/// Этот файл содержит логику для отображения стрелки с построчным чередованием
/// символов в зависимости от заданных параметров.
///
/// \section Input
/// Ввод зависит от режима работы программы.
///
/// **Режим 1:**
/// Пользователь вводит только высоту стрелки.
///
/// **Режим 2:**
/// Пользователь вводит три параметра: высота, ширина краёв и ширина середины стрелки.
///
/// \section Output
/// Программа выводит стрелку с чередующимися символами на каждой строке.
///
/// \section Example
///
/// **Режим 1:**
/// Ввод:
/// 9
///
/// Вывод:
/// #
///  **
///   ###
///    ****
///     #####
///    ****
///   ###
///  **
/// #
///
/// **Режим 2:**
/// Ввод:
/// 9 2 10
///
/// Вывод:
/// ##
///  ****
///   ######
///    ********
///     ##########
///    ********
///   ######
///  ****
/// ##
///
//===----------------------------------------------------------------------===//

#include <stdio.h>

// Функция для отображения стрелки без дополнительных параметров (только высота).
void draw_arrow_simple(int height) {
    int middle = height / 2;  // Определяем середину стрелки
    for (int i = 0; i < height; ++i) {
        // Определяем количество символов и отступ
        int num_symbols = (i <= middle) ? i + 1 : height - i;
        int padding = (i <= middle) ? i : height - i - 1;

        // Печать отступов
        for (int j = 0; j < padding; ++j) {
            printf(" ");
        }

        // Печать символов чередованием
        char symbol = (i % 2 == 0) ? '#' : '*';
        for (int j = 0; j < num_symbols; ++j) {
            printf("%c", symbol);
        }

        printf("\n");
    }
}

// Функция для отображения стрелки с заданной шириной краёв и середины.
void draw_arrow_custom(int height, int edge_width, int middle_width) {
    int middle = height / 2;

    for (int i = 0; i < height; ++i) {
        // Определяем количество символов и отступ
        int num_symbols = (i <= middle) ? edge_width + i * middle_width / middle : edge_width + (height - i - 1) * middle_width / middle;
        int padding = (i <= middle) ? i : height - i - 1;

        // Печать отступов
        for (int j = 0; j < padding; ++j) {
            printf(" ");
        }

        // Печать символов чередованием
        char symbol = (i % 2 == 0) ? '#' : '*';
        for (int j = 0; j < num_symbols; ++j) {
            printf("%c", symbol);
        }

        printf("\n");
    }
}

int main() {
    int mode, height, edge_width, middle_width;

    printf("Введите режим работы (1 - простой, 2 - с параметрами): ");
    scanf("%d", &mode);

    if (mode == 1) {
        // Ввод высоты для простого режима
        printf("Введите высоту стрелки: ");
        scanf("%d", &height);
        draw_arrow_simple(height);
    } else if (mode == 2) {
        // Ввод параметров для расширенного режима
        printf("Введите высоту стрелки, ширину краёв и ширину середины: ");
        scanf("%d %d %d", &height, &edge_width, &middle_width);
        draw_arrow_custom(height, edge_width, middle_width);
    } else {
        printf("Некорректный режим.\n");
    }

    return 0;
}
